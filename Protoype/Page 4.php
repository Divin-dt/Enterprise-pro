        <!-- Text describing the noteable dates in the project's history -->
        <p>
          1870: Education Act enables local councils to open, operate and fund elementary education through the public purse.<br>
          1870: The purpose of the Act was to provide Education where there were insufficient Church School provision. Often called “Filling the Gaps”.<br>
          1870: Bradford set up its School Board in November.<br>
          1873: The School Board opened its first schools in rented buildings.<br>
          1874: Eight School Board schools were built and opened.<br>
          1876: Parents become responsible for ensuring their children learnt the 3 r’s.<br>
          1876: Bradford School Board created an attendance committee.<br>
          1876: Restricted child labour by linking it to passing certain school standards.<br>
