        <p>
          With the incorporation of Thornton into the Bradford Borough the Thornton School Board identified that there would be confusion with two Thornton School names and two Bradford School names. 
          The Thornton Board changed the name of James Street School (to avoid confusion with St James School in Bradford) to Thornton Board School.      
          The Board also recommended that Thornton Lane Board School be changed to Marshfield School to avoid confusion the newly named Thornton Board School. 
