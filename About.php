          The information available to users shows the school through the lens of School Logs Books, Admission Books, Punishment Books, Images and other sources. Using these  sources enables the school history to be viewed from its conception as Chapel Green Board School through to the present Marshfield School.
          Some of the resources included here have been collected and published (though not widely) by Mr D Craven in 2004. Many thanks the D Craven for allowing me to merge my research with that of his own.
          Major resources like the School Logbooks offer a unique window into the past from which general information on the day to day running of the school can be seen. From these comments many social aspects, of the time, can be seen and occasionally the head teacher’s private thoughts or frustrations are expressed. 
          Admission books show when a person entered the school, their fathers name, Standards attained and when the left school. Once again, another source full of social and societal information just waiting to be analysed.
          Noteable dates are intended to show key dates for researchers / interested parties to note.
          Two booklets that have been published about Marshfield School are included in the sources. The first is the ‘Historical Sketch’ of Marshfield School written by J Jackson. The second is ‘Marshfield School: Our heroes of World War 1’ written by Ray Greenhough. To read the two booklets click <a href="Page_1a.php">‘here’</a>
